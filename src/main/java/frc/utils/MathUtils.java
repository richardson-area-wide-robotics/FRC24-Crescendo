package frc.utils;


public class MathUtils {
  
  public double nowNegative(int i)
  {
    return i*-1;
  }

  public double nowNegative(double i)
  {
    return i*-1;
  }

  public double toDouble(float i)
  {
    double o = i;
    return o;
  }

  public double toDouble(int i)
  {
    double o = i;
    return o;
  }
}
